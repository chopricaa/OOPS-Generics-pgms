package oops_concept;
public class Polymorphism {
	public static int add(int n) {
		return n;
	}
	public static int  add(int n,int n1) {
		return n+n1;
	}
	
	public static void main(String args[]) {
		
		
		System.out.println(add(2));
		System.out.println(add(2,5));
		
		
	}
	

}



 




